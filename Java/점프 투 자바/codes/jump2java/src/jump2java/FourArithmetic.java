package jump2java;

public class FourArithmetic {
	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		int i = 0;
		int j = 10;
		i++;
		j--;
		System.out.println(i);
		System.out.println(j);
		
		int k1 = 0;
		System.out.println(k1++);
		System.out.println(k1);
		
		int k2 = 0;
		System.out.println(++k2);
		System.out.println(k2);
	}
}
